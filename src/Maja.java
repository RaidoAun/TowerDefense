public class Maja {

    private int hind;
    private String tanav;

    Maja(int hind, String tanav) {
        this.hind = hind;
        this.tanav = tanav;
    }

    public int getHind() {
        return hind;
    }

    public String getTanav() {
        return tanav;
    }

    public void setHind(int hind) {
        this.hind = hind;
    }

    public void setTanav(String tanav) {
        this.tanav = tanav;
    }

}
